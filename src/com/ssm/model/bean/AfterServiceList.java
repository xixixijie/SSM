package com.ssm.model.bean;

public class AfterServiceList {
    private int after_id;
    private String reason;
    private String requirement;
    private String result;
    private int user_id;
    private int cs_id;
    private int status;
    private String after_date;
    private int order_id;

    public int getAfter_id() {
        return after_id;
    }

    public void setAfter_id(int after_id) {
        this.after_id = after_id;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }

    public String getRequirement() {
        return requirement;
    }

    public void setRequirement(String requirement) {
        this.requirement = requirement;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }

    public int getCs_id() {
        return cs_id;
    }

    public void setCs_id(int cs_id) {
        this.cs_id = cs_id;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getAfter_date() {
        return after_date;
    }

    public void setAfter_date(String after_date) {
        this.after_date = after_date;
    }

    public int getOrder_id() {
        return order_id;
    }

    public void setOrder_id(int order_id) {
        this.order_id = order_id;
    }
}

